    private void ComboBox(){
        Session s = HibernateUtil.getSessionFactory().getCurrentSession();
        s.beginTransaction();
        List<Agencia> lista = (List<Agencia>)s.createQuery("From Agencia").list();
        for(Agencia a: lista){
            CAgencia.addItem(a.getNome());
        }
        s.getTransaction().commit();
    }
    private Cliente Pesquisar(){
        Session s = HibernateUtil.getSessionFactory().getCurrentSession();
        s.beginTransaction();
        Cliente i=null;
        List<Cliente> lista = (List<Cliente>)s.createQuery("From Cliente").list();
        for(Cliente c : lista){
            if(c.getCpf().equals(TCPF.getText())){
                TNome.setText(c.getNome());
                TRG.setText(c.getRg());
                i = c;
                //s.getTransaction().commit();
                break;
            }
        }
        if(i==null){ 
            JOptionPane.showMessageDialog(null, "Cliente nao cadastrado!");
        }
        s.getTransaction().commit();
        System.out.println(i.getNome());
        return i;
    }
    private void Confirmar(){
        Cliente cli =Pesquisar();
        Session s = HibernateUtil.getSessionFactory().getCurrentSession();
        s.beginTransaction();
        Conta conta = new Conta();
        List<Agencia> lista = (List<Agencia>)s.createQuery("From Agencia").list();
        for(Agencia a : lista){
            if(a.getNome().equals(CAgencia.getSelectedItem().toString())){
                conta.setAgencia(a);
                break;
            }
            
        }
        conta.setCliente(cli);
        conta.setSenha(TSenha.getText());
        s.save(conta);
        s.getTransaction().commit();
        NConta();
    }
    
    private void NConta(){
        Session s = HibernateUtil.getSessionFactory().getCurrentSession();
        s.beginTransaction();
        List<Conta> lista = (List<Conta>)s.createQuery("From Conta").list();
        for(Conta c : lista){
            if(c.getCliente().getCpf().equals(TCPF.getText())){
                JOptionPane.showMessageDialog(null, "Conta Cadastrada! /n numero da conta Ã©: "+c.getIdConta()+" numero da agencia agencia: "+c.getAgencia());
            }
        }
        s.getTransaction().commit();
    }
